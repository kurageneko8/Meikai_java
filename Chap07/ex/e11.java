public class e11 {

    public static void main(String[] args) {
        // Scanner in = new Scanner (System.in)
        int x = 3;
        System.out.printf("x=%d\n",x);
        
        // int smallX = x >>> 1;
        int smallX = x >>> 2;
        System.out.printf("smallX=%d\n",smallX);
        
        // int bigX = x << 1;
        int bigX = x << 2;
        System.out.printf("bigX=%d\n",bigX);
        
    }
    
}